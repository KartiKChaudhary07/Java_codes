public class cbPattern{
    public static void main(String[] args){
        int n=5;
        for(int i=0;i<n;i++){
            for(int j=0;j<i-1;j++){
                System.out.println(" ");
            }
            for(int k=0;k<n-1;k++){
                System.out.println("*");

            }
        }
        // System.outprint("");
    }    
    
}